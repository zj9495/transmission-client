import { formatLeftTime } from "./times";

describe.each`
  ms            | expected
  ${1}          | ${"0:00:00"}
  ${1000}       | ${"0:00:01"}
  ${10000}      | ${"0:00:10"}
  ${100000}     | ${"0:01:40"}
  ${1000000}    | ${"0:16:40"}
  ${10000000}   | ${"2:46:40"}
  ${100000000}  | ${"27:46:40"}
  ${1000000000} | ${"277:46:40"}
  ${621658112}  | ${"172:40:58"}
  ${4047585280} | ${"1124:19:45"}
  ${4694212608} | ${"1303:56:52"}
  ${309051392}  | ${"85:50:51"}
  ${439418880}  | ${"122:03:38"}
  ${350371840}  | ${"97:19:31"}
  ${628029680}  | ${"174:27:09"}
  ${Infinity}   | ${"--:--:--"}
`("test formatLeftTime($ms)", ({ ms, expected }) => {
  test(`returns ${expected}`, () => {
    expect(formatLeftTime(ms)).toBe(expected);
  });
});
